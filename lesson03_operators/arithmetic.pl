#!/usr/bin/perl

# Arithmetic Operators

my $a = 10;
my $b = 5;

# Addition
my $sum = $a + $b;
print "Addition: $a + $b = $sum\n";  # Outputs: 10 + 5 = 15

# Subtraction
my $diff = $a - $b;
print "Subtraction: $a - $b = $diff\n";  # Outputs: 10 - 5 = 5

# Multiplication
my $product = $a * $b;
print "Multiplication: $a * $b = $product\n";  # Outputs: 10 * 5 = 50

# Division
my $quotient = $a / $b;
print "Division: $a / $b = $quotient\n";  # Outputs: 10 / 5 = 2

# Modulus (remainder)
my $remainder = $a % $b;
print "Modulus: $a % $b = $remainder\n";  # Outputs: 10 % 5 = 0

# Exponentiation
my $power = $a ** $b;
print "Exponentiation: $a ** $b = $power\n";  # Outputs: 10 ** 5 = 100000
