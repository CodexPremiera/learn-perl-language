#!/usr/bin/perl

# Relational Operators

my $a = 10;
my $b = 5;

# Equal to
print "$a == $b : ", ($a == $b ? "True" : "False"), "\n";  # Outputs: False

# Not equal to
print "$a != $b : ", ($a != $b ? "True" : "False"), "\n";  # Outputs: True

# Greater than
print "$a > $b : ", ($a > $b ? "True" : "False"), "\n";  # Outputs: True

# Less than
print "$a < $b : ", ($a < $b ? "True" : "False"), "\n";  # Outputs: False

# Greater than or equal to
print "$a >= $b : ", ($a >= $b ? "True" : "False"), "\n";  # Outputs: True

# Less than or equal to
print "$a <= $b : ", ($a <= $b ? "True" : "False"), "\n";  # Outputs: False
